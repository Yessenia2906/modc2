package pe.com.bn.modc.exceptions;

public class InternatException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6392534612031094792L;

	public InternatException() {
		super();
 	}

	public InternatException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
 	}

	public InternatException(String message, Throwable cause) {
		super(message, cause);
 	}

	public InternatException(String message) {
		super(message);
 	}

	public InternatException(Throwable cause) {
		super(cause);
 	}

}
