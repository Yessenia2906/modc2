package pe.com.bn.modc.exceptions;

public class ParametrosCompException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = -8800046130082485065L;

	public ParametrosCompException() {
		super();
		 
	}

	public ParametrosCompException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		 
	}

	public ParametrosCompException(String message, Throwable cause) {
		super(message, cause);
		 
	}

	public ParametrosCompException(String message) {
		super(message);
		 
	}

	public ParametrosCompException(Throwable cause) {
		super(cause);
		 
	}

}
