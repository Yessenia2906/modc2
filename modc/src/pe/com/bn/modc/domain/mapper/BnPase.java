	public void asignarValoresDesdeTexto(String textoConEtiquetas,BodyPase paseOutPut ) {
	        // Dividir el texto en etiquetas utilizando el carácter '|'
	        String[] etiquetas = textoConEtiquetas.split("\\|");

	        // Iterar sobre cada etiqueta y asignar valores a los atributos correspondientes
	        for (String etiqueta : etiquetas) {
	            String[] partes = etiqueta.split("-");
	            if (partes.length >= 2) {
	                String nombreAtributo = "set" + partes[1];
	                String valor = paseOutPut.getByTag(etiqueta.trim()).trim();

	                try {
	                    // Utilizar reflexión para llamar al método setter correspondiente
	                    getClass().getMethod(nombreAtributo, String.class).invoke(this, valor);
	                } catch (Exception e) {
	                    e.printStackTrace();  // Maneja excepciones según tus necesidades
	                }
	            }
	        }
	    }
	

